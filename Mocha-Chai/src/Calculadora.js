export default class Calculadora {
    
    //CÁLCULO DE SOMA/ADIÇÃO;
    static soma(a,b) {
        return a + b
    }

    //CÁLCULO DE SUBTRAÇÃO;
    static sub(a,b) {
        return a - b
    }
    
    //CÁLCULO DE MULTIPLICAÇÃO;
    static multi(a,b) {
        return a * b
    }
    
    //CÁLCULO DE DIVISÃO;
    static div(a,b) {
        return a / b
    }
    
    //CÁLCULO DE PORCENTAGEM;
    static porcentagem(a , b){
        return (a/100) * a;
    }
    
    //NÚMERO AO QUADRADO;
    static quad (a) {
        return a * a
    }
}
