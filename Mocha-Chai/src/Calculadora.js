
export default class Calculadora {
    static soma(a,b) {
        return a + b
    }
    static sub(a,b) {
        return a - b
    }
    static multi(a,b) {
        return a * b
    }
    static div(a,b) {
        return a / b
    }
    static porcentagem(a , b){
        return (a/100) * a;
    }
}
